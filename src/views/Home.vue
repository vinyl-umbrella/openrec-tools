<template>
  <div class="home">
    <div class="info">
      <div>最終更新日 2021年6月1日</div> <!-- 注目 -->
      <div>
        保管庫は
        <a
          href="https://drive.google.com/drive/folders/1U77WomTyFEVtFtA4gMQKUJf8gdXRijP8?usp=sharing"
          target="_blank"
          >ここ</a
        >
      </div>
    </div>

    <bar-chart
      id="mainChart"
      :chart-data="graphData"
      :options="options"
      :height="chartHeight"
      :width="chartWidth"
    ></bar-chart>

    <div class="flex-box">
      <div class="flex-items">
        <form>
          <v-row>
            <v-text-field type="number" min="1" max="1000" v-model.number="num" label="表示人数" dense outlined></v-text-field>
          </v-row>
          <v-row>
            <v-text-field type="number" min="2020" max="2021" v-model.number="startYear" label="開始年" dense outlined></v-text-field>
            <v-text-field type="number" min="1" max="12" v-model.number="startMonth" label="開始月" dense outlined></v-text-field>
          </v-row>
          <v-row>
            <v-text-field type="number" min="2020" max="2021" v-model.number="endYear" label="終了年" dense outlined></v-text-field>
            <v-text-field type="number" min="1" max="12" v-model.number="endMonth" label="終了月" dense outlined></v-text-field>
          </v-row>
        </form>
      </div>
      <v-btn @click="createGraphData()" id="update-button" depressed>更新</v-btn>
      <div>{{ inputErrMsg }}</div>
    </div>

    <div>
      <!-- user list -->
      <p v-for="(item, index) in graphData.labels" :key="index">
        {{ index + 1 }}. &nbsp;
        <span @click="clickUser(item)" class="userid">{{ item }}</span>
        &nbsp; {{ graphData.datasets[0].data[index] }}
      </p>
      <modal
        :val="postData"
        :chartData="modalGraphData"
        :chartOptions="options"
        v-show="showModal"
        @close="closeModal()"
      />
    </div>
  </div>
</template>

<script>
import barChart from "../components/barChart";
import firebase from "firebase/app"
import "firebase/firestore"
import Modal from "../components/modal";

const firebaseConfig = require("../secret.json");
firebase.initializeApp(firebaseConfig);
firebase.firestore().clearPersistence();
firebase.firestore().enablePersistence();

export default {
  components: {
    barChart,
    Modal,
  },

  data() {
    return {
      // modal
      showModal: false,
      postData: {
        id: "",
        nickname: "",
        created_at: "",
      },
      modalGraphData: {
        labels: [],
        datasets: [
          {
            data: [],
          },
        ],
      },
      // input default value
      num: 30,
      startYear: 2021,
      startMonth: 5,    // 注目
      endYear: 2021,
      endMonth: 5,      // 注目
      // 入力エラー
      inputErrMsg: "",
      // グラフの描画に使用するデータ
      graphData: {
        labels: [],
        datasets: [
          {
            data: [],
          },
        ],
      },
      options: {
        legend: {
          display: false,
        },
        scales: {
          xAxes: [
            {
              ticks: {
                fontColor: "#ddd",
              }
            }
          ],

          yAxes: [
            {
              ticks: {
                beginAtZero: true,
                fontColor: "#ddd"
              },
            },
          ],
        },
      },
      chartHeight: window.innerHeight * 0.3,
      chartWidth: window.innerWidth * 0.8,
    };
  },

  mounted() {
    this.createGraphData();
  },

  methods: {
    async clickUser(userid) {
      let url = "https://public.openrec.tv/external/api/v5/channels/" + userid;
      const res = await fetch(url);
      if (res.ok) {
        let j = await res.json();
        this.postData["id"] = j["id"];
        this.postData["nickname"] = j["nickname"];
        this.postData["created_at"] = j["registered_at"];
      } else {
        this.postData["id"] = userid;
        this.postData["nickname"] = "";
        this.postData["created_at"] = "account not found";
      }

      let temp = {
        labels: [],
        datasets: [
          {
            data: [],
            backgroundColor: "#0288D1",
            hoverBackgroundColor: "#03A9F4",
          },
        ],
      }
      // 注目
      temp["labels"] = ["2020-7", "2020-8", "2020-9", "2020-10", "2020-11", "2020-12", "2021-1", "2021-2", "2021-3", "2021-4", "2021-5"];
      for (let i of temp["labels"]) {
        let ym = i.split("-");
        const doc = await this.getFromFirestore(ym[0], ym[1]);
        if (doc[userid]) {
          temp["datasets"][0]["data"].push(doc[userid]);
        } else {
          temp["datasets"][0]["data"].push(0);
        }
      }
      // obj を丸ごと変更するとグラフに反映される
      this.modalGraphData = temp;
      this.showModal = true;
    },

    closeModal() {
      this.showModal = false;
    },

    // ラベルとそのデータを用いてグラフを更新
    updataGraph(label, data) {
      this.graphData = {
        labels: label,
        datasets: [
          {
            data: data,
            backgroundColor: "#0288D1",
            hoverBackgroundColor: "#03A9F4",
          },
        ],
      };
    },

    // 数字を0付き2桁の文字列に変換
    getDoubleDigestNumber(num) {
      return ("0" + num).slice(-2);
    },

    // 入力年月をチェック
    // 注目
    checkYearMonth() {
      if (this.startYear >= 2020 && this.startYear <= 2021) {
        if (this.endYear >= 2020 && this.endYear <= 2021) {
          if (this.startMonth >= 1 && this.startMonth <= 12) {
            if (this.endMonth >= 1 && this.endMonth <= 12) {
              if (
                Number(
                  String(this.endYear) +
                    this.getDoubleDigestNumber(this.endMonth)
                ) >=
                Number(
                  String(this.startYear) +
                    this.getDoubleDigestNumber(this.startMonth)
                )
              ) {
                this.inputErrMsg = "";
                return true;
              }
            }
          }
        }
      }
      this.inputErrMsg = "入力値エラー";
      return false;
    },

    async getFromFirestore(year, month) {
      const db = firebase.firestore();

      try {
        const doc = await db.collection(String(year)).doc(String(month)).get({
          source: "cache",
        });
        if (doc.empty) {
          // console.log("no data");
          return {};
        }
        return doc.data();
      } catch (err) {
        const doc = await db.collection(String(year)).doc(String(month)).get({
          source: "server",
        })
        return doc.data();
      }
    },

    async createGraphData() {
      if (this.checkYearMonth()) {
        let ids = [];
        let counts = [];
        let graphDataObj = {};
        let yearMonth = [];
        let y = Number(this.startYear);
        let m = Number(this.startMonth);

        // 取得するyear, monthを格納した配列を生成
        while (
          Number(
            String(this.endYear) + this.getDoubleDigestNumber(this.endMonth)
          ) >= Number(String(y) + this.getDoubleDigestNumber(m))
        ) {
          yearMonth.push([y, m]);
          if (m == 12) {
            y += 1;
            m = 1;
          } else {
            m += 1;
          }
        }

        for (let elem of yearMonth) {
          // console.log(elem);
          // 各月のデータを取得
          let doc = await this.getFromFirestore(elem[0], elem[1]);

          // 存在するなら加算 else 追加
          for (let key of Object.keys(doc)) {
            if (graphDataObj[key]) {
              graphDataObj[key] += Number(doc[key]);
            } else {
              graphDataObj[key] = Number(doc[key]);
            }
          }
        }

        // idとcountペアの配列をpush
        let tempArray = [];
        for (let key of Object.keys(graphDataObj)) {
          tempArray.push([key, graphDataObj[key]]);
        }
        // ソートする
        tempArray.sort(function (a, b) {
          return b[1] - a[1];
        });

        // カット
        if (this.num < 1) {
          this.num = 1;
        }
        let n = Math.min(tempArray.length, this.num);
        tempArray = tempArray.slice(0, n);

        // id と count の配列に分ける
        for (let i of tempArray) {
          ids.push(i[0]);
          counts.push(i[1]);
        }

        this.updataGraph(ids, counts);
      }
    },
  },
}
</script>

